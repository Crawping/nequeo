CREATE TABLE [dbo].[Company] (
    [CompanyID]          BIGINT        IDENTITY (1, 1) NOT NULL,
    [CompanyName]        VARCHAR (500) NOT NULL,
    [CompanyDescription] VARCHAR (MAX) NULL,
    [ABNReference]       VARCHAR (100) NULL,
    [ContactName]        VARCHAR (200) NULL,
    [ContactPosition]    VARCHAR (200) NULL,
    [Address1]           VARCHAR (500) NULL,
    [Address2]           VARCHAR (500) NULL,
    [Suburb]             VARCHAR (50)  NULL,
    [StateID]            BIGINT        NULL,
    [PostCode]           VARCHAR (5)   NULL,
    [PhoneNumber]        VARCHAR (50)  NULL,
    [FaxNumber]          VARCHAR (50)  NULL,
    [EmailAddress]       VARCHAR (500) NULL,
    [WebSite]            VARCHAR (500) NULL,
    [PostalAddress1]     VARCHAR (500) NULL,
    [PostalAddress2]     VARCHAR (500) NULL,
    [PostalSuburb]       VARCHAR (50)  NULL,
    [PostalStateID]      BIGINT        NULL,
    [PostalPostCode]     VARCHAR (5)   NULL,
    [CountryID]          BIGINT        NOT NULL,
    [ModifiedDate]       DATETIME      NULL,
    [RowVersionData]     ROWVERSION    NULL,
    CONSTRAINT [PK_Company] PRIMARY KEY CLUSTERED ([CompanyID] ASC),
    CONSTRAINT [FK_Company_Country] FOREIGN KEY ([CountryID]) REFERENCES [dbo].[Country] ([CountryID]),
    CONSTRAINT [FK_Company_PostalState] FOREIGN KEY ([PostalStateID]) REFERENCES [dbo].[State] ([StateID]),
    CONSTRAINT [FK_Company_State] FOREIGN KEY ([StateID]) REFERENCES [dbo].[State] ([StateID])
);

